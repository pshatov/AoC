def main() -> None:

    with open('input.txt') as f:
        f_line = f.readline().strip()

    xx = [int(x) for x in f_line.split(',')]
    x_min, x_max = min(xx), max(xx)

    dict_fuel1, dict_fuel2 = {}, {}
    for tx in range(x_min, x_max + 1):
        f1, f2 = 0, 0
        for x in xx:
            d = abs(tx - x)
            f1 += d
            f2 += d * (d + 1) // 2
        dict_fuel1[tx], dict_fuel2[tx] = f1, f2

    print("part 1: %d" % min(dict_fuel1.values()))
    print("part 2: %d" % min(dict_fuel2.values()))
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    main()
