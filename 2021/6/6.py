from typing import Dict


# ---------------------------------------------------------------------------------------------------------------------
def step(fish: Dict[int, int]) -> None:

    fish_new = {}

    for i in range(9):
        fish_new[i] = 0

    for i in range(1, 9):
        fish_new[i - 1] = fish[i]
    fish_new[8] += fish[0]
    fish_new[6] += fish[0]

    for i in range(9):
        fish[i] = fish_new[i]
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
def main() -> None:

    with open('input.txt') as f:
        f_line = f.readline()

    fish = {}
    for i in range(9):
        fish[i] = 0

    for f in f_line.strip().split(','):
        fish[int(f)] += 1

    for i in range(80):
        step(fish)

    print("part 1: %d" % sum(fish.values()))

    for i in range(256 - 80):
        step(fish)

    print("part 2: %d" % sum(fish.values()))

# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    main()
