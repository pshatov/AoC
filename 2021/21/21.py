# ---------------------------------------------------------------------------------------------------------------------
from typing import Tuple
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
class DeterministicDice:

    # -----------------------------------------------------------------------------------------------------------------
    def __init__(self):
        self._num_rolls = 0
        self._value = 0
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def roll(self) -> int:

        self._num_rolls += 1

        t = self._value

        self._value += 1
        self._value %= 100

        return t + 1
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    @property
    def num_rolls(self) -> int:
        return self._num_rolls
    # -----------------------------------------------------------------------------------------------------------------
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
def move(p: int, s: int, d: DeterministicDice) -> Tuple[int, int]:

    a = d.roll()
    b = d.roll()
    c = d.roll()
    d = a + b + c

    p += d
    p %= 10

    s += (p + 1)

    return p, s
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
def main() -> None:

    with open('input.txt') as f:
        fl1, fl2 = [t.strip() for t in f.readlines()]

    p1 = int(fl1.split(':')[1]) - 1
    p2 = int(fl2.split(':')[1]) - 1

    dd = DeterministicDice()

    s1, s2 = 0, 0
    while True:
        p1, s1 = move(p1, s1, dd)
        if s1 >= 1000:
            z = s2 * dd.num_rolls
            break
        p2, s2 = move(p2, s2, dd)
        if s2 >= 1000:
            z = s1 * dd.num_rolls
            break

    print("part 1: %d" % z)


# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    main()
