from typing import List, Tuple, Set


# ---------------------------------------------------------------------------------------------------------------------
# Types
# ---------------------------------------------------------------------------------------------------------------------
Route = List[str]
Edge = Tuple[str, ...]


# ---------------------------------------------------------------------------------------------------------------------
def build_path_subroutes(path: Route, edges: Set[Edge]) -> List[Route]:

    next_nodes: List[str]
    next_nodes = []
    for e in edges:
        nn = None
        if e[0] == path[-1]:
            nn = e[1]
        if e[1] == path[-1]:
            nn = e[0]

        if nn is None:
            continue

        if nn.islower() and nn in path:
            continue

        next_nodes.append(nn)

    subroutes = []
    for nn in next_nodes:
        path_nn = path + [nn]
        if nn == 'end':
            subroutes.append(path_nn)
        else:
            subroutes += build_path_subroutes(path_nn, edges)

    return subroutes
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
def main() -> None:

    with open('input.txt') as f:
        f_lines = [t.strip() for t in f.readlines()]

    nodes = set()
    edges = set()
    for fl in f_lines:
        fl_nodes = fl.split('-')
        for n in fl_nodes:
            nodes.add(n)
        edges.add(tuple(fl_nodes))

    routes = build_path_subroutes(['start'], edges)
    print("part 1: %d" % len(routes))
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    main()
