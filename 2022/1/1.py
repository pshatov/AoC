# ---------------------------------------------------------------------------------------------------------------------
class Food:

    # -----------------------------------------------------------------------------------------------------------------
    def __init__(self) -> None:
        self._calories = []
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def add_calories(self, calories: int) -> None:
        self._calories.append(calories)
    # -----------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------
    def total_calories(self) -> int:
        return sum(self._calories)
    # -----------------------------------------------------------------------------------------------------------------
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
def main() -> None:

    all_foods = []

    with open('input.txt') as f:
        all_lines = [l.strip() for l in f.readlines()]

    last_value = -1
    for next_line in all_lines:
        if not next_line:
            last_value = -1
            continue
        if last_value == -1:
            all_foods.append(Food())
        last_value = int(next_line)
        all_foods[-1].add_calories(last_value)

    max_value = max([f.total_calories() for f in all_foods])
    print("part 1: %d" % max_value)
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    main()
