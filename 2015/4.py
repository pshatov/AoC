import hashlib


# ---------------------------------------------------------------------------------------------------------------------
def main() -> None:

    with open('input_4.txt') as f:
        f_line = f.readline().strip()

    mine, mine5, mine6 = 0, -1, -1
    while mine5 < 0 or mine6 < 0:
        s = f_line + str(mine)
        h = hashlib.md5(s.encode('utf-8')).hexdigest()

        if h.startswith('00000'):

            if mine5 < 0:
                mine5 = mine

            if h.startswith('000000'):
                mine6 = mine

        mine += 1

    print("part 1: %d" % mine5)
    print("part 2: %d" % mine6)
# ---------------------------------------------------------------------------------------------------------------------


# ---------------------------------------------------------------------------------------------------------------------
if __name__ == '__main__':
    main()
